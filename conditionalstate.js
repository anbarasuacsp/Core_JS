// Switch

var day = 'a'; 

switch(day){
    case 1:
        console.log('This is monday');
        break;
    case 2:
        console.log('This is Tueday');
        break;
    case 3: 
        console.log('This is Wednesday');
        break;
    case 4:
        console.log('This is Thursday');
        break;
    case 5:
        console.log('This is friday');
        break;
    default:
        console.log('Entered value undefined');

}

